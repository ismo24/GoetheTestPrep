  export const getTranslation = (category, key, language = TRANSLATIONS.DEFAULT_LANGUAGE) => {
    try {
      return TRANSLATIONS[category][key][language] || TRANSLATIONS[category][key][TRANSLATIONS.DEFAULT_LANGUAGE] || key;
    } catch (error) {
      console.warn(`Translation not found for ${category}.${key}.${language}`);
      return key;
    }
  };
  
